package net.zomis.javatips.classes;

public class ClassWithMultipleFields {

    private String someIdentifier;
    private String someOtherIdentifier;
    private String thirdIdentifier;
    private String something;
    private String somethingElse;

    public String getSomeIdentifier() {
        return someIdentifier;
    }

    public void setSomeIdentifier(String someIdentifier) {
        this.someIdentifier = someIdentifier;
    }

    public String getSomeOtherIdentifier() {
        return someOtherIdentifier;
    }

    public void setSomeOtherIdentifier(String someOtherIdentifier) {
        this.someOtherIdentifier = someOtherIdentifier;
    }

    public String getThirdIdentifier() {
        return thirdIdentifier;
    }

    public void setThirdIdentifier(String thirdIdentifier) {
        this.thirdIdentifier = thirdIdentifier;
    }

    public String getSomething() {
        return something;
    }

    public void setSomething(String something) {
        this.something = something;
    }

    public String getSomethingElse() {
        return somethingElse;
    }

    public void setSomethingElse(String somethingElse) {
        this.somethingElse = somethingElse;
    }
}
